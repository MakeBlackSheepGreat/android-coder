enum class AppLanguage(val code: String, val displayName: String) {
    ZH("zh", "中文"),
    EN("en", "English");

    companion object {
        fun fromCode(code: String): AppLanguage {
            return entries.firstOrNull { it.code == code } ?: ZH
        }
    }
}

